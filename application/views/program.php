    $(document).ready(function() {
      var list = $(".card-kegiatan");
      var numToShow = 3;
      var buttonLoadMore = $("#loadMore");
      var buttonShowLess = $("#showLess");
      var numInList = list.length;

      list.hide();
      if (numInList > numToShow) {
        buttonLoadMore.show();
      }

      list.slice(0, numToShow * 2).show(); // Menampilkan 6 kartu pertama

      buttonLoadMore.click(function() {
        var showing = list.filter(":visible").length;
        list.slice(showing, showing + numToShow).fadeIn();
        var nowShowing = list.filter(":visible").length;
        if (nowShowing >= numInList) {
          buttonLoadMore.hide();
          buttonShowLess.show();
        }
      });

      buttonShowLess.click(function() {
        list.hide();
        list.slice(0, numToShow * 2).show(); // Menampilkan 6 kartu pertama
        buttonLoadMore.show();
        buttonShowLess.hide();
      });
    });
  </script>
